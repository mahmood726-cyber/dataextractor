const ConfidenceCalibrator = {
    // Pre-trained weights for confidence calibration
    weights: {
        intercept: -0.5,
        sourceTable: 0.8,
        sourceProse: 0.3,
        sourceAbstract: 0.6,
        hasCI: 0.4,
        hasN: 0.3,
        hasPValue: 0.35,
        inAbstract: 0.5,
        nearKeyword: 0.4,
        multipleMatches: -0.3,
        lengthPenalty: -0.1,
        exactMatch: 0.5,
        numericValue: 0.3
    },

    // Field-specific calibration weights
    fieldWeights: {
        hazardRatio: { baseConfidence: 0.7, ciBonus: 0.15, tableBonus: 0.1 },
        pValue: { baseConfidence: 0.8, exactBonus: 0.1 },
        sampleSize: { baseConfidence: 0.75, multipleSourceBonus: 0.15 },
        age: { baseConfidence: 0.7, rangeBonus: 0.1 },
        followUp: { baseConfidence: 0.65, unitBonus: 0.15 },
        drug: { baseConfidence: 0.8, knownDrugBonus: 0.1 },
        biomarker: { baseConfidence: 0.7, unitBonus: 0.1, valueBonus: 0.1 },
        outcome: { baseConfidence: 0.65, compositeBonus: 0.15 }
    },

    // Sigmoid function
    sigmoid(x) {
        // Clamp to prevent overflow
        const clampedX = Math.max(-500, Math.min(500, x));
        return 1 / (1 + Math.exp(-clampedX));
    },

    // Basic calibrate confidence score
    calibrate(features) {
        let logit = this.weights.intercept;

        if (features.source === 'table') logit += this.weights.sourceTable;
        if (features.source === 'prose') logit += this.weights.sourceProse;
        if (features.source === 'abstract') logit += this.weights.sourceAbstract;
        if (features.hasCI) logit += this.weights.hasCI;
        if (features.hasN) logit += this.weights.hasN;
        if (features.hasPValue) logit += this.weights.hasPValue;
        if (features.inAbstract) logit += this.weights.inAbstract;
        if (features.nearKeyword) logit += this.weights.nearKeyword;
        if (features.multipleMatches) logit += this.weights.multipleMatches;
        if (features.textLength > 10000) logit += this.weights.lengthPenalty;
        if (features.exactMatch) logit += this.weights.exactMatch;
        if (features.numericValue) logit += this.weights.numericValue;

        return this.sigmoid(logit);
    },

    // Field-specific calibration
    calibrateField(fieldType, extraction, context) {
        const fieldConfig = this.fieldWeights[fieldType] || { baseConfidence: 0.6 };
        let confidence = fieldConfig.baseConfidence;

        // Apply field-specific bonuses
        if (fieldType === 'hazardRatio') {
            if (extraction.ciLo !== undefined && extraction.ciHi !== undefined) {
                confidence += fieldConfig.ciBonus || 0;
            }
            if (context.source === 'table') {
                confidence += fieldConfig.tableBonus || 0;
            }
        } else if (fieldType === 'pValue') {
            if (/[=<>]\s*0\.\d+/.test(extraction.value)) {
                confidence += fieldConfig.exactBonus || 0;
            }
        } else if (fieldType === 'sampleSize') {
            if (context.matchCount > 1) {
                confidence += fieldConfig.multipleSourceBonus || 0;
            }
        } else if (fieldType === 'biomarker') {
            if (extraction.unit) {
                confidence += fieldConfig.unitBonus || 0;
            }
            if (typeof extraction.value === 'number') {
                confidence += fieldConfig.valueBonus || 0;
            }
        }

        // Apply general context adjustments
        if (context.inAbstract) confidence += 0.05;
        if (context.nearKeyword) confidence += 0.03;
        if (context.multipleMatches) confidence -= 0.05;

        // Clamp confidence to valid range
        return Math.min(0.99, Math.max(0.01, confidence));
    },

    // Calibrate extraction result (legacy support)
    calibrateExtraction(extraction, context) {
        const features = {
            source: extraction.source || 'prose',
            hasCI: extraction.ciLo !== undefined && extraction.ciHi !== undefined,
            hasN: extraction.n !== undefined,
            hasPValue: extraction.pValue !== undefined,
            inAbstract: context.position < 2000,
            nearKeyword: context.nearKeyword || false,
            multipleMatches: context.matchCount > 1,
            textLength: context.textLength || 0,
            exactMatch: extraction.exactMatch || false,
            numericValue: typeof extraction.value === 'number'
        };

        return this.calibrate(features);
    },

    // Edge case handling
    handleEdgeCase(extraction, context) {
        // Handle missing values
        if (extraction.value === null || extraction.value === undefined) {
            return { confidence: 0.01, reason: 'missing_value' };
        }

        // Handle out-of-range values
        if (typeof extraction.value === 'number') {
            // HR should be > 0
            if (context.fieldType === 'hazardRatio' && extraction.value <= 0) {
                return { confidence: 0.01, reason: 'invalid_hr' };
            }
            // Sample size should be positive integer
            if (context.fieldType === 'sampleSize' && (extraction.value < 1 || !Number.isInteger(extraction.value))) {
                return { confidence: 0.05, reason: 'invalid_sample_size' };
            }
            // Age should be reasonable
            if (context.fieldType === 'age' && (extraction.value < 0 || extraction.value > 120)) {
                return { confidence: 0.05, reason: 'invalid_age' };
            }
        }

        // Handle conflicting values
        if (context.conflictingValues && context.conflictingValues.length > 0) {
            return { confidence: 0.3, reason: 'conflicting_values' };
        }

        return null; // No edge case detected
    }
};

